package Command;

class TVFunciones {
	public void encender() {
		System.out.println("La televisión se ha prendido");
	}
	public void apagar() {
		System.out.println("La televisión se ha apagado");
	}
	public void subirVolumen() {
		System.out.println("Se ha subido el volumen de la televisión");
	}
	public void bajarVolumen() {
		System.out.println("Se ha bajado el volumen de la televisión");	
	}
	public void mute() {
		System.out.println("Se ha muteado la televisión");
	}
} 
